    private void mettreAJourGrilleGraphique() {

    for (int i = 0; i < grille.getNbLignes(); i++) {
        for (int j = 0; j < grille.getNbColonnes(); j++) {
            Cellule cellule = grille.matriceCellules[i][j];
            Cellule_Graphique bouton = (Cellule_Graphique) jPanel1.getComponent(i * grille.getNbColonnes() + j);

            if (cellule.estDevoilee()) {
                if (cellule.getPresenceBombe()) {
                    bouton.setText("üí£");
                } else {
                    bouton.setText(String.valueOf(cellule.getNbBombesAdjacentes()));
                }
            } else {
                bouton.setText("");
            }
        }
    }
}


    private void initialiserGrilleGraphique() {
            jPanel1.setLayout(new java.awt.GridLayout(grille.getNbLignes(), grille.getNbColonnes()));

        for (int i = 0; i < grille.getNbLignes(); i++) {
            for (int j = 0; j < grille.getNbColonnes(); j++) {
                Cellule_Graphique bouton = new Cellule_Graphique(i, j, grille.matriceCellules[i][j]);
                bouton.addActionListener(creerActionListener(bouton));

                jPanel1.add(bouton); // Ajouter chaque bouton √† jPanel1
            }
        }
    }

    private ActionListener creerActionListener(Cellule_Graphique bouton) {
    return e -> {
        int ligne = bouton.x;
        int colonne = bouton.y;

        Cellule cellule = grille.matriceCellules[ligne][colonne];

        if (!cellule.estDevoilee()) {
            if (cellule.getPresenceBombe()) {
                // Bombe cliqu√©e : afficher un message de d√©faite
                bouton.setText("üí£");
                JOptionPane.showMessageDialog(this, "BOUM ! Game Over !");
                System.exit(0);
            } else {
                // R√©v√©ler les cellules (logique)
                grille.revelerCelluleParCoordonnees(ligne, colonne);

                // Mettre √† jour l'interface graphique
                mettreAJourGrilleGraphique();

                // V√©rifier si toutes les cellules non-bombes sont d√©voil√©es
                if (grille.toutesCellulesRevelees()) {
                    JOptionPane.showMessageDialog(this, "F√©licitations, vous avez gagn√© !");
                    System.exit(0);
                }
            }
        }
    };
}



